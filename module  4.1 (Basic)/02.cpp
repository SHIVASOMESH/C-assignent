#include<iostream>
using namespace std;

int main()
{
	cout<<"2. What is OOP? List OOP concepts "<<endl;
	cout<<"A:- OOP is Object-Oriented Programming"<<endl;
	cout<<"=> OOP concepts in C++"<<endl;
	cout<<"=> Class: A class is a user-defined data type that contains data members and member functions. It is used to create objects."<<endl;
	cout<<"=> Object: An object is an instance of a class. It has its own data and methods that operate on that data."<<endl;
	cout<<"=> Encapsulation: Encapsulation is the process of hiding implementation details of a class from other parts of the program. This is achieved by making the data members private and providing public member functions to access and manipulate them."<<endl;
	cout<<"=> Inheritance: Inheritance is a mechanism by which one class can inherit properties and methods from another class. The class that inherits is called the derived class, and the class that is inherited from is called the base class."<<endl;
	cout<<"=> Polymorphism: Polymorphism is the ability of objects to take on many forms. In C++, this is achieved through virtual functions and function overloading."<<endl;
	cout<<"=> Abstraction: Abstraction is the process of reducing complex details and only showing the essential features of an object. This is achieved by using abstract classes and pure virtual functions."<<endl;
	cout<<"=> Constructor: A constructor is a special member function that is called when an object is created. It initializes the data members of the object."<<endl;	
	cout<<"=> Destructor: A destructor is a special member function that is called when an object is destroyed. It frees any resources that the object was using."<<endl;
	
	return 0;
}
